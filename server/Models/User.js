const mongoose = require("mongoose");

const UserSchema = mongoose.Schema({
  Email: {
    type: String,
    required: true,
    unique: true,
  },
  username: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
});

module.exports = mongoose.model("User", UserSchema, "users");
